// Render text as an array of 5 strings (one per font row).
export function renderFiglet(text) {
  const rows = ['', '', '', '', ''];
  const chars = text.toUpperCase().split('');
  for (const ch of chars) {
    const glyph = FIGLET[ch] || FIGLET[' '];
    for (let r = 0; r < FIGLET_HEIGHT; r++) {
      rows[r] += (glyph[r] || '   ') + ' ';
    }
  }
  return rows;
}
